import java.util.Scanner;
import java.util.HashSet;
import java.util.List;
import java.util.Collections;

/** Class to compare 2 sets of integers, the program will capture all the numbers in
the first set until it finds a 0, then it will continue capturing the 2nd set until
the 2nd 0. the output will be the items in set 2 which are not present in set 1.*/ 

class SetComparator {

	public static void main(String[] args) {
		HashSet<Integer> set1 = new HashSet<Integer>(), set2 = new HashSet<Integer>();
		Scanner in = new Scanner(System.in);
		int input;
		while((input = in.nextInt())!=0){
			set1.add(input);
		}
		while((input = in.nextInt())!=0){
			set2.add(input);
		}

		int missingCount = 0;
		System.out.println("Missing elements in 1st set:");
		for(Integer i : set2){
			if(!set1.contains(i)){
				++missingCount;
				System.out.println(i);
			}
		}
		System.out.println("Total missing: "+missingCount);
	}
}


